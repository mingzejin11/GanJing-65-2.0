// Encoder function definition
bool encoder_update_user(uint8_t index, bool clockwise) {
	// On function layer, knob controls brightness
	if (IS_LAYER_ON(FN)) {
		if (clockwise) {
			tap_code(KC_BRIU);
		} else {
			tap_code(KC_BRID);
		}

	// Otherwise, knob controls volume by default
	} else {
		if (clockwise) {
			tap_code(KC_VOLU);
		} else {
			tap_code(KC_VOLD);
		}
	}

	return false;
}

// Macro functions
